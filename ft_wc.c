#include "libft.h"

int	ft_wc(const char *s, char c)
{
	int wc;
	int i;

	i = 0;
	wc = 0;
	while (s[i] == c)
		i++;
	while (s[i])
	{
		wc++;
		while (s[i] && s[i] != c)
			i++;
		while (s[i] && s[i] == c)
			i++;
	}
	return (wc);
}
