#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (s2[i] == '\0')
		return (s1);
	while (s1[i] != '\0' && n-- > 0)
		i++;
	while (s1[j] != '\0' && n-- > 0)
	{
		s1[j + i] = s2[j];
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
